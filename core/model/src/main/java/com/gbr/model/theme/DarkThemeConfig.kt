package com.gbr.model.theme

/**
 * Represents the dark theme configuration options for the app.
 */
enum class DarkThemeConfig {
    /**
     * Follow the system's dark theme setting
     */
    FOLLOW_SYSTEM,

    /**
     * Always use light theme
     */
    LIGHT,

    /**
     * Always use dark theme
     */
    DARK
}
